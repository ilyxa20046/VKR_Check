function parseFormulaDisplay(text: string) {
  const match = text.match(/^(.*?),\s*\(([^)]+)\)\s*$/);
  if (!match) {
    return { expression: text, number: "" };
  }

  return { expression: match[1], number: `(${match[2]})` };
}

function Fraction({ numerator, denominator }: { numerator: ReactNode; denominator: ReactNode }) {
  return (
    <span className="mx-1 inline-flex align-middle">
      <span className="inline-flex flex-col items-center leading-none">
        <span className="border-b border-black px-1 pb-0.5">{numerator}</span>
        <span className="pt-0.5">{denominator}</span>
      </span>
    </span>
  );
}

function formatFormulaToHtml(expression: string) {
  const greekMap: Array<[RegExp, string]> = [
    [/\balpha\b/g, "α"],
    [/\bsigma\b/g, "σ"],
    [/\blambda\b/g, "λ"],
    [/\beta\b/g, "β"],
    [/\bgamma\b/g, "γ"],
    [/\beta_hat\b/g, "β̂"],
  ];

  const protectedWords = ["min", "mod", "sign", "prod", "sum", "sqrt", "erfc", "BER", "BLER", "SNR", "LLR", "AWGN", "Rayleigh", "NMS", "ZF"];

  let html = expression
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/\*+/g, " · ")
    .replace(/->/g, "→")
    .replace(/>=/g, "≥")
    .replace(/<=/g, "≤")
    .replace(/\^\(([^)]+)\)/g, "<sup>$1</sup>")
    .replace(/_\{([^}]+)\}/g, "<sub>$1</sub>")
    .replace(/\^\{([^}]+)\}/g, "<sup>$1</sup>")
    .replace(/_([A-Za-z0-9]+)/g, "<sub>$1</sub>")
    .replace(/\^([A-Za-z0-9]+)/g, "<sup>$1</sup>")
    .replace(/sqrt\(([^)]+)\)/g, "√($1)");

  for (const [pattern, symbol] of greekMap) {
    html = html.replace(pattern, symbol);
  }

  const placeholders = new Map<string, string>();
  protectedWords.forEach((word, idx) => {
    const token = `§${idx}§`;
    placeholders.set(token, word);
    html = html.replace(new RegExp(`\\b${word}\\b`, "g"), token);
  });

  html = html
    .replace(/(^|[^A-Za-zА-Яа-я])([A-Za-z])(?=<sub>|<sup>|[^A-Za-zА-Яа-я]|$)/g, "$1<i>$2</i>")
    .replace(/\s+/g, " ")
    .trim();

  placeholders.forEach((word, token) => {
    html = html.replace(new RegExp(token.replace(/[§]/g, "\\$&"), "g"), word);
  });

  return html;
}

